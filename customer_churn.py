'''handle columns non numeric to numeric'''
def handle_non_numerical_data(cleaned_df):
    columns = cleaned_df.columns.values
    for column in columns:
        text_digit_vals = {}
        def convert_to_int(val):
            return text_digit_vals[val]

        if cleaned_df[column].dtype != np.int64:
            column_contents = cleaned_df[column].values.tolist()
            unique_elements = set(column_contents)
            x = 0
            for unique in unique_elements:
                if unique not in text_digit_vals:
                    text_digit_vals[unique] = x
                    x+=1

            cleaned_df[column] = list(map(convert_to_int, cleaned_df[column]))

    return cleaned_df

